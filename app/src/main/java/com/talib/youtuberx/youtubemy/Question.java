package com.talib.youtuberx.youtubemy;

public class Question {
    private String sul, bri, ki, cu, dor, du, categoryId, sek;

    public Question() {
    }

    public Question(String sul, String bri, String ki, String cu, String dor, String du, String categoryId, String sek) {
        this.sul = sul;
        this.bri = bri;
        this.ki = ki;
        this.cu = cu;
        this.dor = dor;
        this.du = du;
        this.categoryId = categoryId;
        this.sek = sek;
    }

    public String getSul() {
        return sul;
    }

    public void setSul(String sul) {
        this.sul = sul;
    }

    public String getBri() {
        return bri;
    }

    public void setBri(String bri) {
        this.bri = bri;
    }

    public String getKi() {
        return ki;
    }

    public void setKi(String ki) {
        this.ki = ki;
    }

    public String getCu() {
        return cu;
    }

    public void setCu(String cu) {
        this.cu = cu;
    }

    public String getDor() {
        return dor;
    }

    public void setDor(String dor) {
        this.dor = dor;
    }

    public String getDu() {
        return du;
    }

    public void setDu(String du) {
        this.du = du;
    }

    public String getCategoryId() {
        return categoryId;
    }

    public void setCategoryId(String categoryId) {
        this.categoryId = categoryId;
    }

    public String getSek() {
        return sek;
    }

    public void setSek(String sek) {
        this.sek = sek;
    }
}


